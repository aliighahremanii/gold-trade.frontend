const DEFAULT_LOCALE = "en-US";

export function formatTimestamp(value: string, locale: string = DEFAULT_LOCALE): string {
  const date = new Date(value);

  if (Number.isNaN(date.getTime())) {
    return value;
  }

  return new Intl.DateTimeFormat(locale, {
    dateStyle: "medium",
    timeStyle: "short",
  }).format(date);
}
